
// Arrow Functions
// A regular function but looks cool 


// Old function
let myFunction = function() {
  return "hello"
};


// Arrow function
let coolFunction = () => {
  return "hi"
};


// Can also do one liners without curly braces
let shortFunction = () => "hey"

// Output: 
// hello hi hey


console.log(myFunction(), coolFunction(), shortFunction())







